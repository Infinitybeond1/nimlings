# More variables 
# Another type of variable is defined with let 
# A variable defined with let is immutable meaning it can't be changed 
# 
# let x = 10 
# x = 11 
#
# The above code will throw an error because you cant change the value of a variable defined with let 
# 
# Change the code so that x = 10
let x = 10 
x = 11 

assert x == 10 
echo x
